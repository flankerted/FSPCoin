// +build windows

package storage

import (
	"unsafe"

	"golang.org/x/sys/windows"
)

func GetDiskFreeSize(path string) uint64 {
	size := uint64(0)
	h := windows.MustLoadDLL("kernel32.dll")
	c := h.MustFindProc("GetDiskFreeSpaceExW")
	lpFreeBytesAvailable := uint64(0)
	lpTotalNumberOfBytes := uint64(0)
	lpTotalNumberOfFreeBytes := uint64(0)
	_, _, err := c.Call(uintptr(unsafe.Pointer(windows.StringToUTF16Ptr(path))),
		uintptr(unsafe.Pointer(&lpFreeBytesAvailable)),
		uintptr(unsafe.Pointer(&lpTotalNumberOfBytes)),
		uintptr(unsafe.Pointer(&lpTotalNumberOfFreeBytes)))
	// 返回值:"The operation completed successfully."
	if err != nil {
		size = lpTotalNumberOfFreeBytes
	}
	// log.Info("", "free(G)", size/gSize)
	return size
}
